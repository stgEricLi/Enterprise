function plusDate(val) {
    var date = new Date(val);
    date.setDate(date.getDate() + 1);
    //var date = plusDate.toISOString().substr(0, 10);
    //d = parseInt(v.StartDay.replace(regx, "")) + 1;
    //v.StartDay.match(regx) + d.toString()
    var y = date.getFullYear();
    var m = date.getMonth() + 1;
    var d = date.getDate();
    return y.toString() + "/" + padLeft("00", m.toString()) + "/" + padLeft("00", d.toString());;
}

function padLeft(nth, str) {
    var res = nth.substring(0, nth.length - str.length) + str;
    return res
}

function getActInfo(actid){
    $.ajax({
        type: "POST",
        url: "../../../mcpservices/SvAct.asmx/get_All_Act_Details",
        contentType: "application/json; charset=utf-8",
        dataType: "json",
        data: JSON.stringify({ "filtervalue": actid }),
        cache: false,
        asyn: false,
        success: function (response) {
            if (response.hasOwnProperty('d')) {
                var json = JSON.parse(response.d);
                var objAct = json.Acts;
                //console.log(objAct);   
                $('.intro').html('');
                $('h1').html(objAct[0].Name);
                var sb = '', d = 0;
                var regx = /\d{4}\/\d{2}\//g;
                sb += '<ul>';
                $.each(objAct, function (i, v) {                    
                    if (actid.substring(0, 2) == 'E2') {                    
                        sb += '<li>活動日期：<span class="badge pricing">' + v.StartDay + ' - ' + plusDate(v.StartDay) + '</span></li>';
                    } else {
                        sb += '<li>活動日期：<span class="badge pricing">' + v.StartDay + '</span></li>';
                    }
                    sb += '<li>活動天數：' + actid.substring(1, 2) + '天</li>';
                    sb += '<li>報名日期：即日起至 ' + v.RegExpDay + ' 止。</li>';
                    sb += '<li>報名方式：採線上報名<span class="emphize">【報名方式：以車為單位，報滿即停止報名】</span>。</li>';
                    if (actid == 'E2000000036') {
                        sb += '<li>本梯次最少需滿 16 人才可開梯。</li>';
                    } else {
                        sb += '<li>本梯次最少需滿 30 人才可開梯。</li>';
                    }
                    
                });
                sb += '</ul>';
                $('.intro').html(sb);
            } 
        },
        error: function (xhr, textStatus, thrownError) {
            console.log(xhr.status);
            console.log(xhr.responseText);
            console.log(thrownError);           
        }
    });
}